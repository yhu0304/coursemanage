	@Override
	public void doFilter(ServletRequest request, ServletResponse response,
			FilterChain filterChain) throws IOException, ServletException {
		// TODO Auto-generated method stub
		try{
			HttpServletRequest httpRequest = (HttpServletRequest) request;
			HttpServletResponse httpResponse = (HttpServletResponse) response;
			//isValid初始值为true
			boolean isValid = true;
			//将路径转换为大写
			String uriStr = httpRequest.getRequestURI().toUpperCase();
			//如果登录页面不是index.jsp，且Session中username为空，那么isValid值为false
			if (uriStr.indexOf("INDEX") == -1 && httpRequest.getSession().getAttribute("userid")
					== null){
				isValid = false;
			}
			if(isValid){
				filterChain.doFilter(request, response);
			} else {
				httpResponse.sendRedirect("/CourseManage/index.jsp");
			}
		} catch (ServletException sx) {
			filterConfig.getServletContext().log(sx.getMessage());
		} catch (IOException iox) {
			filterConfig.getServletContext().log(iox.getMessage());
		}

	}

	

